// Area of Circle :
// ====> WE USE FUNCTION METHOD AND DEFINE AREA OF CIRCLE : <=======

function areaofCircle(r: number) {
  let areaofCircle = Math.PI * r ** 2; // <=== we use Math formula :
  return areaofCircle;
}

let radius = areaofCircle(4); // <==== change radius and change result:
console.log(" \n\tArea of Circle is : ", radius);
